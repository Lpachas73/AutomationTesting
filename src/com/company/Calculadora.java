package com.company;

public class Calculadora {


    static double sumar2Numeros(double a, double b){
        return a+b;
    }

    static double resta2Numeros(double a, double b){
        return a-b;
    }

    static double multiplica2Numeros(double a, double b){
        return a*b;
    }

    static double divide2Numeros(double a, double b){
        return a/b;
    }

    static boolean sonIguales(double a, double b){
        return a == b;
    }
}
